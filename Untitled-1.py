
#Функция проверки числа
def check_number(num):
    if num > 0:
        return "Число положительное."
    elif num < 0:
        return "Число отрицательное."
    else:
        return "Число равно нулю."

#Функция вычисления факториала числа
def factorial(num):
    #Факториал может быть определен только для положительных чисел
    if num < 0:
        return "Факториал не определен для отрицательных чисел."
    elif num == 0:
        return 1
    else:
        fact = 1  # Начальное значение факториала
        for i in range(1, num + 1):  #Цикл от 1 до числа включительно
            fact *= i  #Умножение текущего значения на i
        return fact

#Функциявычисления квадрата числа
def square(num):
    square_value = num ** 2  #Возведение числа в квадрат
    return square_value

#Функция для проверки, является ли число простым
def is_prime(num):
    if num < 2:  #Числа меньше 2 не являются простыми
        return False
    for i in range(2, int(num ** 0.5) + 1):  #Проверка делителей до квадратного корня из числа
        if num % i == 0:  #Если число делится на любое i, оно не является простым
            return False
    return True  #Если делителей нет, то число простое

# Основная программа
def main():
    #Ввод числа с обработкой возможных ошибок
    try:
        number = int(input("Введите целое число: "))  #Ввод целого числа
    except ValueError:
        print("Ошибка: введите корректное целое число.")  #Если ввод некорректен, вывод сообщения
        return  #Прекращение выполнения программы в случае ошибки
    
    #Проверка числа и вывод результата
    result = check_number(number)  #Вызов функции для проверки числа
    print(result)  #Вывод результата (положительное, отрицательное или ноль)
    
    #Дополнительные действия с числом, если оно не равно нулю
    if number != 0:
        print("\nВыберите действие:")
        print("1. Вычислить факториал")
        print("2. Вычислить квадрат числа")
        print("3. Проверить, является ли число простым")

        choice = input("Введите номер действия (1, 2 или 3): ")  #Выбор действия 

        if choice == '1':
            factorial_result = factorial(number)  #Вызов функции для опредиления факториала
            print(f"Факториал числа {number} равен: {factorial_result}")
        elif choice == '2':
            square_result = square(number)  #Вызов функции для вычисления квадрата числа
            print(f"Квадрат числа {number} равен: {square_result}")
        elif choice == '3':
            prime_check = is_prime(number)  #Вызов функции для проверки простоты числа
            if prime_check:
                print(f"Число {number} является простым.")
            else:
                print(f"Число {number} не является простым.")
        else:
            print("Неверный выбор. Попробуйте снова.")  #Если выбор был неверный 
    else:
        print("Для числа 0 дополнительные действия не выполняются.")  #Если число равно 0, ничего не выполняется

#Запуск программы
if __name__ == "__main__":
    main()